use crate::game::{HashSet, Point};

#[derive(Debug, Clone)]
pub struct CampaignMap {
    pub width: u16,
    pub height: u16,
    pub layout: &'static [&'static str],
}

#[must_use]
pub fn get_campaign_map(level: u32, map_width: u16, map_height: u16) -> HashSet<Point> {
    let mut obstacles = HashSet::new();
    if level == 0 {
        return obstacles;
    }
    let map_idx = (level as usize).saturating_sub(1) % CAMPAIGN_MAPS.len();
    let map = &CAMPAIGN_MAPS[map_idx];
    let offset_x = map_width.saturating_sub(map.width) / 2;
    let offset_y = map_height.saturating_sub(map.height) / 2;
    for (y, row) in map.layout.iter().enumerate() {
        for (x, ch) in row.chars().enumerate() {
            if ch == '#' {
                obstacles.insert(Point {
                    x: u16::try_from(x).unwrap_or(0) + offset_x,
                    y: u16::try_from(y).unwrap_or(0) + offset_y,
                });
            }
        }
    }
    obstacles
}

